jQuery(document).ready(()=>{
    const instructions = `Un estudiante universitario decide comenzar a trabajar mientras cursa sus estudios, ya que necesita generar ingresos para cubrir sus gastos personales. Esta decisión implica reorganizar su tiempo, enfrentar nuevas exigencias y equilibrar distintas responsabilidades que antes no tenía.<br><br>
                          A medida que avanza el semestre, el estudiante se da cuenta de que la situación no se reduce únicamente a “tener o no tener trabajo”, sino que involucra varios aspectos interrelacionados que deben ser considerados para poder sostener esta decisión en el tiempo.<br><br>
                          Indica si cada afirmación corresponde a una parte del problema que el estudiante debe considerar (Verdadero) o si no corresponde, sino que es un resultado, comparación u objetivo (Falso).`;

    /////////////////////////INTRO CON INSTRUCCIONES////////////////////////////
    $('body').prepend(`<div class="intro">
                            <div class="text-content">
                                <div class="intro-text">${instructions}</div>
                                <a class="intro-btn">Comenzar</a>
                            </div>
                        </div>`);

    $('.intro-btn').on('click',(e)=>{
        $('.intro').animate({
                marginTop:`-${$('.h5p-container').outerHeight()}px`,
        }, 1000);

        setTimeout(() => {
            $('.intro').css({'display':'none'});
        }, 1000);
    });

    /////////////////////////PESTAÑA CON INSTRUCCIONES PARA H5P/////////////////////////////

    $('.h5p-content').prepend(`<div class="custom-instructions">
                                    <div class="tab-container">
                                        <div id="tab" class="tab">
                                            <div class="tab-content">
                                                <div class="instructions-text">${instructions}</div>
                                            </div>
                                            <a class="instructions-btn" href="">Instrucciones</a>
                                        </div>
                                    </div>
                                </div>`);

    $('.instructions-btn').on('click',(e)=>{
        e.preventDefault();

        if(parseInt($('.custom-instructions').css('margin-top').split("px")[0]) == 0){
            $('.custom-instructions').animate({
                marginTop:`-${$('.tab-content').outerHeight()}px`,
            }, 1000);
        }else if(parseInt($('.custom-instructions').css('margin-top').split("px")[0]) < 0){
            $('.custom-instructions').animate({
                marginTop:'0',
            }, 1000);
        }
    });

    setTimeout(() => {
        $('.custom-instructions').animate({
            marginTop:`-${$('.tab-content').outerHeight()}px`,
        }, 1000);
    }, 500);
});